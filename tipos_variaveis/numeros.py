# Inteiro
numero_inteiro = 10
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 10.0
print("Real com ponto flutuante =", numero_real)

# type() - Descobrir o tipo da variável que está utilizando
print("Tipo da variavel inteiro =", type(numero_inteiro))
print("Tipo da variavel real =", type(numero_real))

# Operações aritméticas

# SOMA
soma = 1 + 1
print("1 + 1 =", soma)

# SUBTRAÇÃO
subtracao = 1 - 1
print("1 - 1 =", subtracao)

# MULTIPLICAÇÃO
multiplicacao = 2 * 2
print("2 x 2 =", multiplicacao)

# DIVISÃO
divisao = 5 / 2
print("5 / 2 =", divisao)
# DIVISÃO RETORNA SOMENTE INT
divisao_int = 5 // 2
print("5 // 2 =", divisao_int)

# int()
print("Valor em inteiro =", int(divisao))

# float()
print("Valor em float =", float(multiplicacao))

# MODULO
modulo = 5 % 2
print("Resto da divisão 5 / 2 =", modulo)
